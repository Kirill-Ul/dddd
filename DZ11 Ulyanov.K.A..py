pattern = r'(\d{2}\s\d{2}\s\d{6}|\d{2}\s\d{2}\s\d{3}\s\d{3}|\d{2}-\d{2}-\d{3}-\d{3}|\d{2}-\d{2}-\d{6}\b)'
test_string = '''
Допускаются следующие форматы записи:
●	00 00 000000
●	00 00 000 000
●	00-00-000-000
●	00-00-000000


'''
passport = (re.findall(pattern, test_string))
print(passport)



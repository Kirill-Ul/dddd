# Задача 1: Поиск серии и номера паспорта
# Дана произвольная строка. Написать программу, которая с использованием регулярных выражений находит все серии и номера паспорта в строке. Допускаются следующие форматы записи:
# ●	00 00 000000
# ●	00 00 000 000
# ●	00-00-000-000
# ●	00-00-000000
# На месте 0 может стоять любая цифра. Результат должен быть представлен в виде списка найденных серий и номеров.
#

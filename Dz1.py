# # Пользователь вводит сумму вклада в банк и годовой процент. Найдите сумму вклада через 5 лет
#
summa = float(input('Введите сумму вклада в рублях')) #запрашиваем у пользователя сумму вклада
procent = float(input('Введите годовой процент')) # запрашиваем процент
result = (summa * (procent / 100)/365*1825) # формула расчета за 5 лет( 1825 - это количество дней за пять лет )
print(f'За 5 лет Ваш вклад составит:{result} рублей')

# Дано трехзначное число. Найти произведение цифр
#
chislo = int(input('Введите трёхначное число: '))
# разбиваем число на цифры
a = int(chislo // 100) # делим число на цело,остаток отбрасываем
b = int(chislo // 10 % 10) # делим число на цело,остаток отбрасываем, потом делим и берем остаток от деления
c = int(chislo % 10) # делим и берем остаток от деления
proizvedenie = a * b * c # умножаем
print (f"Произведение цифр числа {chislo} равно:{proizvedenie}")




